#python program to demonstrate Accessing of elements in a set
#creation a set
set1 = set(['Goal','for','life'])
print("\nInitial set")
print(set1)

#accessing element using for loop
print("\nElement of set:")
for i in set1:
    print(i, end=" ")
print("\n")

#checking the element using in keyword
print("checking the element using in keyword ")
print("Goal" in set1)

