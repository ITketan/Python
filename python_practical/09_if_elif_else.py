#9) WAP for if,elif,else

book = input("Enter book name: ")

if(book == 'PHP' or book == 'php'):
    print("You choose "+ book + " book")
elif(book == 'JAVA' or book == 'java'):
    print("You choose " + book + " book")
elif(book == 'ANDROID' or book == 'android'):
    print("You choose " + book + " book")
else:
    print("You choose invalid book")
